package types

import "fmt"

func PrintXLSX(sheetName string, header []string, body interface{}) {
	fmt.Println("================================================================================================================================")
	fmt.Println("--------------------------------------------------------------------------------------------------------------------------------")
	for i, v := range header {
		fmt.Print(v, " | ")
		if i == len(header)-1 {
			fmt.Println()
		}
	}
	fmt.Println("--------------------------------------------------------------------------------------------------------------------------------")

	b, ok := body.([][]string)
	if ok {
		for _, row := range b {
			for i, col := range row {
				fmt.Print(col, " | ")
				if i == len(row)-1 {
					fmt.Println()
				}
			}
			fmt.Println("--------------------------------------------------------------------------------------------------------------------------------")
		}
	} else {
		fmt.Println(body)
	}
	fmt.Println("================================================================================================================================")
	fmt.Println("")
}
